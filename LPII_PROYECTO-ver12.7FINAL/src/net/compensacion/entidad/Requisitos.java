package net.compensacion.entidad;

public class Requisitos {
	private int codRequisitos;
	private String decJurada;
	private String resCont;
	private String resQuinq;
	private String constPago;
	private String dni;
	private int codSolicitud;
	
	public Requisitos() {
		
	}

	public int getCodRequisitos() {
		return codRequisitos;
	}

	public void setCodRequisitos(int codRequisitos) {
		this.codRequisitos = codRequisitos;
	}

	public String getDecJurada() {
		return decJurada;
	}

	public void setDecJurada(String decJurada) {
		this.decJurada = decJurada;
	}

	public String getResCont() {
		return resCont;
	}

	public void setResCont(String resCont) {
		this.resCont = resCont;
	}

	public String getResQuinq() {
		return resQuinq;
	}

	public void setResQuinq(String resQuinq) {
		this.resQuinq = resQuinq;
	}

	public String getConstPago() {
		return constPago;
	}

	public void setConstPago(String constPago) {
		this.constPago = constPago;
	}

	public String getDni() {
		return dni;
	}

	public void setDni(String dni) {
		this.dni = dni;
	}

	public int getCodSolicitud() {
		return codSolicitud;
	}

	public void setCodSolicitud(int codSolicitud) {
		this.codSolicitud = codSolicitud;
	}

	
}
